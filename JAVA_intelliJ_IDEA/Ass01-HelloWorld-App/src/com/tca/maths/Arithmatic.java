package com.tca.maths;

public class Arithmatic
{
    public  int num1,num2;

    public Arithmatic(int num1,int num2)
    {
        this.num1=num1;
        this.num2=num2;
    }

    public int sum()
    {
        return num1+num2;
    }

    public  int sub()
    {
        return  num1+num2;
    }

}
